def execute_function(string1):
    print(string1)
    ui=string1.split(' ')
    ans=[]
    for i in ui:
        temp=''
        for j in i:
            temp+=morse_code[j]
            temp+=' '
        ans.append(temp)
    return "   ".join(ans)

